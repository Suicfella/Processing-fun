package processing;

public class LearningHex {

    public static void main(String[] args) {
        int first = 65536 * 10;
        int second = 4096 * 15;
        int third = 256 * 14;
        int fourth = 16 * 14;
        int fifth = 1 * 15;
        int sum = first + second + third + fourth + fifth;
        System.out.println(sum);
    }

}
